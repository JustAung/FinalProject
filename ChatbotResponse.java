public class ChatbotResponse {
    private String text;

        // Constructor
        public ChatbotResponse(String text) {
            this.text = text;
        }

        // Getter for the text of the response
        public String getText() {
            return text;
        }

        // Setter for the text of the response
        public void setText(String text) {
            this.text = text;
    }
}

